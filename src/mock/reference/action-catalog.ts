/**
 * @/mock/reference/action-catalog — the consolidated remediation-action library.
 *
 * REFERENCE DATA (not per-tenant state). ~600 candidate-action strings across the
 * research failure-catalog collapse to this de-duplicated, parameterized set
 * (docs/06 §7, §14): every "Force differential merge" is one `force-merge`,
 * every reauth-consent is one `reauthorize-oauth`, etc. Each action declares the
 * asset kinds / product types it applies to, its params, reversibility, approval
 * rule, dry-run support, outcome class (self-heal | guidance-only | opens-ticket),
 * an estimated duration, and a lucide icon name.
 *
 * `outcome` drives the simulated runner (docs/06 §11):
 *   - self-heal     → mutates the target toward `protected`, auto-resolves the alert
 *   - guidance-only → checklist/runbook, no asset mutation
 *   - opens-ticket  → assembles a support package, asset stays in its failed state
 *
 * The ids here are the referential-integrity backbone: every id referenced by
 * `failure-modes.ts` MUST exist in `ACTION_CATALOG`.
 */

