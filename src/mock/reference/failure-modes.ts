// ─────────────────────────────────────────────────────────────────────────────
// Derive + assemble the typed FailureMode[]
// ─────────────────────────────────────────────────────────────────────────────

const FREQ_MAP: Record<Row["freq"], FailureMode["frequency"]> = {
  "very common": "very-common",
  common: "common",
  occasional: "occasional",
  rare: "rare",
};

/** Category families that imply a cosmetic-by-default class when paired with timing language. */
function isCosmeticByDefault(row: Row): boolean {
  if (row.cosmetic) return true;
  // Low-severity screenshot/verification timing failures default to cosmetic.
  return (
    row.cat === "Screenshot/Local Verification" && row.sev === "low"
  );
}

/** Modes whose category is a transient/auto-merge class where Datto self-heals. */
function vendorAutoHeals(row: Row): boolean {
  if (row.auto) return true;
  return row.cat === "Diff-Merge / Chain Rebuild";
}

function buildMode(row: Row): FailureMode {
  const category = normalizeCategory(row.cat);
  return {
    id: row.id,
    productType: row.pt,
    category,
    title: row.title,
    description:
      row.desc ??
      `${row.title}. Classified under ${category} for ${row.pt}; matched from verbatim signals in the failing run/alert.`,
    defaultSeverity: catalogToSeverity(row.sev),
    catalogSeverity: row.sev,
    frequency: FREQ_MAP[row.freq],
    cosmeticByDefault: isCosmeticByDefault(row),
    vendorAutoHeals: vendorAutoHeals(row),
    selfServiceable: row.self,
    matchSignals: signals(row.err),
    remediationActionIds: row.act,
    runbookRef: `runbook-${row.id}`,
  };
}

/** The full typed failure catalog (135 modes). */
export const FAILURE_MODES: FailureMode[] = ROWS.map(buildMode);

/** Fast id → FailureMode lookup. */
export const FAILURE_MODE_BY_ID: Record<string, FailureMode> = Object.fromEntries(
  FAILURE_MODES.map((m) => [m.id, m]),
);

// ─────────────────────────────────────────────────────────────────────────────
// Referential integrity — fail loud at module load if any action id is unknown
// or any failure id is duplicated. Determinism + correctness guard.
// ─────────────────────────────────────────────────────────────────────────────

(() => {
  // FAILURE_MODE_BY_ID is keyed by id ALONE, so ids must be globally unique —
  // a repeat (even across products) silently overwrites a distinct mode in the
  // lookup. Enforce global uniqueness so that class of corruption fails loud.
  const seen = new Set<string>();
  for (const m of FAILURE_MODES) {
    if (seen.has(m.id)) {
      throw new Error(`[failure-modes] duplicate FailureMode id: ${m.id}`);
    }
    seen.add(m.id);
    if (m.remediationActionIds.length === 0) {
      throw new Error(`[failure-modes] ${m.id} references no remediation actions`);
    }
    for (const aid of m.remediationActionIds) {
      if (!ACTION_IDS.has(aid)) {
        throw new Error(
          `[failure-modes] ${m.id} references unknown action id "${aid}" (referential integrity)`,
        );
      }
    }
  }
})();

// ─────────────────────────────────────────────────────────────────────────────
// fixTypeForMode — the Issue-builder classifier (docs/00 §3)
// ─────────────────────────────────────────────────────────────────────────────

/**
 * Classify the fix type for a failure mode given the actions it offers.
 *
 *   - "full"     — the mode is self-serviceable and *every* offered action that
 *                  isn't pure read-only guidance self-heals → End-to-end fix.
 *   - "partial"  — self-serviceable but the remediation mixes automated "We"
 *                  self-heal steps with manual "You" guidance → Guided fix.
 *   - "external" — not self-serviceable but the path opens a ticket / external
 *                  flow → Insights only.
 *   - "manual"   — not self-serviceable and the path is pure guidance → Insights only.
 *   - "unknown"  — no offered actions at all (unclassified).
 *
 * `actions` is the resolved RemediationAction[] for the mode (use ACTION_BY_ID).
 */
export function fixTypeForMode(
  mode: FailureMode,
  actions: RemediationAction[],
): FixType {
  if (actions.length === 0) return "unknown";

  const hasSelfHeal = actions.some((a) => a.outcome === "self-heal");
  const hasTicket = actions.some((a) => a.outcome === "opens-ticket");
  const hasGuidance = actions.some((a) => a.outcome === "guidance-only");

  if (!mode.selfServiceable) {
    // We can't auto-fix; either we assist a ticket or just hand a runbook.
    return hasTicket ? "external" : "manual";
  }

  // Self-serviceable from here.
  if (hasSelfHeal && !hasTicket && !hasGuidance) return "full";
  if (hasSelfHeal) return "partial";
  // Self-serviceable but no self-heal action resolved — treat as guidance.
  return hasTicket ? "external" : "manual";
}

/** Convenience: resolve a mode's actions then classify. */
export function fixTypeForModeId(modeId: string): FixType {
  const mode = FAILURE_MODE_BY_ID[modeId];
  if (!mode) return "unknown";
  const actions = mode.remediationActionIds
    .map((id) => ACTION_BY_ID[id])
    .filter((a): a is RemediationAction => Boolean(a));
  return fixTypeForMode(mode, actions);
}
