/** Catalog severity → design-system Severity token (docs/06 §2). */
export function catalogToSeverity(c: CatalogSeverity): Severity {
  switch (c) {
    case "critical":
    case "high":
      return "critical";
    case "medium":
    case "low":
    default:
      return "warning";
  }
}

/** Normalize the two research category strings that don't match the enum. */
function normalizeCategory(raw: string): FailureCategory {
  if (raw === "Diff-Merge / Chain Rebuild") return "Diff-Merge/Chain Rebuild";
  if (raw === "API/Authentication") return "OAuth/Auth";
  return raw as FailureCategory;
}

/** Build matchSignals from a list of verbatim error/code strings. */
function signals(codes: string[]): MatchSignal[] {
  return codes.map((c) => {
    // Treat tokens that look like a code/hex/HTTP status as errorCode, else message.
    const isCode = /^(0x|AADSTS|VSS_E|BKP|BAK|AGT|SNS|ZFS|bk\d|\d{3}\b|Code\s|stop\s|STOP\s)/.test(
      c,
    );
    return {
      field: isCode ? "errorCode" : "errorMessage",
      contains: c,
    };
  });
}

